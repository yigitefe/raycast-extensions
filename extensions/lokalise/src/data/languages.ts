export function getLanguageName(isoCode: string): string {
  if (!isoCode) {
    return "Unknown";
  }

  // Normalize: lowercase and replace dashes with underscores
  const normalizedCode = isoCode.toLowerCase().replace(/-/g, "_");

  // Try exact match first
  if (languageMap[normalizedCode]) {
    return languageMap[normalizedCode];
  }

  // Try matching the base language code (e.g., "zh" from "zh_tw")
  const baseCode = normalizedCode.split("_")[0];
  if (baseCode && languageMap[baseCode]) {
    return languageMap[baseCode];
  }

  // Fallback to uppercase ISO code
  return isoCode.toUpperCase();
}
